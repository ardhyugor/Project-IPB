<style>
  @keyframes scrollFadeIn {
    from {
      opacity: 0;
      transform: translateY(30px);
    }
    to {
      opacity: 1;
      transform: translateY(0);
    }
  }

  .scroll-fade-in {
    opacity: 0;
    transform: translateY(30px);
  }

  .scroll-fade-in.animate {
    animation: scrollFadeIn 0.8s ease-out forwards;
  }
</style>

<script>
  // Intersection Observer untuk deteksi element masuk viewport saat scroll
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        // Element masuk viewport, tambahkan class animate
        entry.target.classList.add('animate');
      }
    });
  }, {
    threshold: 0.1 // Trigger ketika 10% element terlihat
  });

  // Observer semua element dengan class scroll-fade-in
  document.querySelectorAll('.scroll-fade-in').forEach((element) => {
    observer.observe(element);
  });
</script>